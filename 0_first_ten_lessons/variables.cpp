#include <iostream>

using namespace std;

int main() {
    //integer (num)
    int x;
    x=5;

    cout<<x<<endl;

    //double (num with dec)
    double price;
    price = 16.8952;

    cout<<price<<endl;

    //char(single character)
    char grade='A';
    char initial='M';

    cout<<grade<<endl;
    cout<<initial<<endl;

    //boolean (true or false)
    bool student = true;
    bool power = false;

    cout<<student<<endl;
    cout<<power<<endl;

    //string
    string name = "Bro";

    cout<<"Hello "<<name<<endl;

    return 0;
}